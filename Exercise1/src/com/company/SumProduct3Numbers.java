package com.company;

public class SumProduct3Numbers {
    public static void main(String[] args) {
        int number1 = 1, number2 = 2;
        int number3 = 77;
        int sum, product;

        sum = number1 + number2 + number3;
        product = number1 * number2 * number3;

        System.out.println(sum);
        System.out.println(product);
    }
}
